import { GetStaticProps } from 'next'
import Head from 'next/head'
import { Fragment } from 'react'
import { getProposalsByStage } from '../api/proposals'
import { SanitizedHtml } from '../components/SanitizedHtml'
import { ProposalsByStage, stages } from '../types'

interface Props {
  proposals: ProposalsByStage
}

export const getStaticProps: GetStaticProps = async () => {
  const proposals = await getProposalsByStage()
  const oneHourInSeconds = 1 * 60 * 60

  return {
    props: { proposals },
    revalidate: oneHourInSeconds
  }
}

export default function Proposals({ proposals }: Props) {
  if (typeof window !== 'undefined') {
    console.log({ proposals })
  }

  return (
    <>
      <Head>
        <title>EcmaScript Proposals</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      {stages.map((stage) =>
        proposals[stage].map((proposal) => (
          <Fragment key={proposal.proposalHtml}>
            <span>Stage: {stage}</span>
            <br />
            <span>Type: {proposal.type}</span>
            <SanitizedHtml html={proposal.proposalHtml} />
            <SanitizedHtml html={proposal.authorsHtml} />
            <SanitizedHtml html={proposal.championsHtml} />
            <SanitizedHtml html={proposal.meetingNotesHtml} />
            <SanitizedHtml html={proposal.expectedPublicationYearHtml} />
            <SanitizedHtml html={proposal.rationaleHtml} />
            <SanitizedHtml html={proposal.lastPresentedHtml} />
            <br />
          </Fragment>
        ))
      )}
    </>
  )
}
