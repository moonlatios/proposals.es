import { GetStaticProps } from 'next'
import Head from 'next/head'
import styled from 'styled-components'
import { getAllProposalsByStage } from '../api/proposals'
import { StageCard } from '../components/proposals/StageCard'
import { ProposalsByStage, stages } from '../types'

const Container = styled.div`
  display: flex;
  justify-content: center;
  gap: 3rem;
  margin: 1rem auto;
  flex-wrap: wrap;
  width: 90%;
`

interface Props {
  proposals: ProposalsByStage
}

export const getStaticProps: GetStaticProps = async () => {
  const proposals = await getAllProposalsByStage()
  const oneHourInSeconds = 1 * 60 * 60

  return {
    props: { proposals },
    revalidate: oneHourInSeconds
  }
}

export default function ProposalsPage({ proposals }: Props) {
  if (typeof window !== 'undefined') {
    console.log({ proposals })
  }

  return (
    <>
      <Head>
        <title>EcmaScript Proposals</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Container>
        {stages.map((stage) => (
          <StageCard stage={stage} proposals={proposals[stage]} key={stage} />
        ))}
      </Container>
    </>
  )
}
